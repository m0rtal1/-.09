        "Хирургия": "Проводит диагностику и лечение хирургических заболеваний: грыж, травм, воспалительных процессов. Используются щадящие методы, обеспечивающие быстрое восстановление.",
        "Эндокринология": "Занимается гормональными нарушениями у детей: задержкой роста, ожирением, сахарным диабетом, болезнями щитовидной железы. Коррекция обмена веществ важна для гармоничного развития.",
        "Кардиология": "Диагностирует и лечит заболевания сердца и сосудов: одышку, боли в груди, обмороки, нарушения давления. Раннее выявление помогает предотвратить осложнения.",
